//одиночное включение-выключение формирования flyback 

function DRCU_SinglePS(delay)
{ 
dev.w(150,0);
dev.c(55);
dev.w(150,1);
 print("Включение");
 dev.c(54);
 
 sleep (delay);
dev.w(150,0);
 print("Выключение");
 dev.c(54);
 sleep (100);
}

//-------------------------------------------------------------------------------------------------------------------------------------------
//Включение разрядки БП 

function DRCU_PSDischarge() //Включение разрядки БП
{
	dev.w(150,0);
	dev.c(55);
	
while(!anykey())

	{
	dev.w(150,1);
	dev.c(55);
	sleep(100);
	}

	dev.w(150,0);
	dev.c(55);
	
}

