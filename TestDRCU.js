//Ресурсный тест

function DRCU_Test(N)
{
	Time = [];
	Time[-1] = 0;
	for (var i = 0; i < N; i++)
	{
		for (var j = 0; j < 11; j++)
		{
			k = i*11+j;
			Time[k] = new Date();
			p("#" + (k) + " / "+ (i + 1) );
			p("№ dI/dt = " + CurrentRateArray[j]);
			p("Время паузы между импульсами, с: " + (Time[k] - Time[k-1])/1000);
			p("----------------");
			p("");
			
			if(!DRCU_Pulse(CurrentTest, CurrentRateArray[j]))
			return;
			
				
			
			if (anykey())
				return;
		}
	}
}

