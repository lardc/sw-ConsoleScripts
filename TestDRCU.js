//Выставление напряжения на внутреннем источнике 

function DRCU_PSSetpoint(voltage)
{
	dev.w(130,voltage*10);

	while(!anykey())
	{
		p(dev.r(201));
		sleep(2000);
	}
	dev.w(130,0);
}

